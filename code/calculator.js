//create a function calculator with basic math operators

function addOperator(a, b) {
    return a + b;
}

function subOperator(a, b) {
    return a - b;
}

function divOperator(a, b) {
    return a / b;
}

function multOperator(a, b) {
    return a * b;
}

export { addOperator, subOperator, divOperator, multOperator }